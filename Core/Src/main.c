void HAL_TIM_IC_CaptureCallback(TIM_HandleTypeDef *htim) // колбек по захвату
{
        if(htim->Instance == TIM1)
        {
                if(htim->Channel == HAL_TIM_ACTIVE_CHANNEL_2) // FALLING с HIGH на LOW
                {
                        snprintf(trans_str, 127, "Pulse %lu mks\r\n", ic_ccr);
                        //HAL_UART_Transmit(&huart2, (uint8_t*)trans_str, strlen(trans_str), 1000);
                        write_data(trans_str);
                        
                }
        }

        if(htim->Instance == TIM3) {
          uint32_t counter = __HAL_TIM_GET_COUNTER(htim);
          double angle = 2*3.14/32;
          uint8_t direct = __HAL_TIM_IS_TIM_COUNTING_DOWN(htim);
          double omega = angle/(HAL_GetTick()-start_time)*1000;
          omega = direct == 0 ? omega : -omega;
          start_time = HAL_GetTick();
          snprintf(trans_str, 63, "Encoder %lu %.2f\r\n", counter, omega);
          write_data(trans_str);
        }
}

